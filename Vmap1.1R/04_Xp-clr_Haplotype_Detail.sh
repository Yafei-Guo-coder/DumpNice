#1. 保护祖先种的意义: 祖先种中存在的变异，在传播的过程中丢失。
  #eg(抗病): Sr33(TraesCS1D02G029100), Sr45(TraesCS1D02G040400), Tsn1(TraesCS5B02G059000)
